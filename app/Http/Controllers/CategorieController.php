    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        foreach (Produit::all()->where('categorie_id',$id) as $produit) {
            $produit->categorie_id = null;
            $produit->save();
        }
        $cat = Categorie::find($id);
        $cat->delete();
        return redirect('/category')->with('success','Product Category supprimé');
    }
